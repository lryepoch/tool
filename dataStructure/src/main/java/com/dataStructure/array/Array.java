public class Array {

	public static void main(String[] args) {
		
		int [] a={1,2,3};
		for(int i:a){
			System.out.print(i+" ");
		}
		System.out.println();
		for(int i=0;i<a.length;i++)
			System.out.print(a[i]+" ");

	}

}
